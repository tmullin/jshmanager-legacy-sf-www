<p>
JSHManager's source code may be retrieved via Subversion. Run the following
command to check out the latest (and possibly unstable) sources:
</p>
<pre>
svn co https://jshm.svn.sourceforge.net/svnroot/jshm/trunk jshm 
</pre>
<p>
At this time,
source archives are not created with each binary distribution, but you
can download the source for a particular release with the following command:
</p>
<pre>
svn co https://jshm.svn.sourceforge.net/svnroot/jshm/tags/JSHManager-x.y.z jshm 
</pre>
<p>
<a href="http://jshm.svn.sourceforge.net/viewvc/jshm/tags/">Browse SVN</a> to see
a full list of tags.
</p>
<p>
For more details, see <a href="https://sourceforge.net/svn/?group_id=240590">SourceForge's SVN information</a>
or <a href="http://jshm.svn.sourceforge.net/viewvc/jshm/">browse the SVN root</a>.
</p>
