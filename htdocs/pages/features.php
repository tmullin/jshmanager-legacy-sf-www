<h2>Implemented Features</h2>
<ul>
	<li>GH and RB single-player support</li>
	<li>Download and view song data (i.e. number of notes, base score, cutoffs) from ScoreHero</li>
	<li>Download and view scores from ScoreHero</li>
	<li>Enter new scores and upload them to ScoreHero</li>
	<li>Delete locally saved scores</li>
	<li>A patch system so you don't have to redownload ~8 mb for each new release</li>
	<li>Specify picture/video URLs for new scores</li>
	<li>Open picture and video URLs in browser</li>
	<li>Allow scores for multiple difficulties/instruments to be downloaded in one shot in the score download wizard</li>
	<li>CSV score import</li>
	<li>Internal image viewer to open image URLs from within JSHManager</li>
	<li>Notification of new version</li>
	<li>Internal viewer for user-submitted SP paths in the <a href="http://wiki.scorehero.com">ScoreHero Wiki</a></li>
</ul>

<h2>Features to be Implemented Relatively Soon</h2>
<ul>
	<li>Delete scores from ScoreHero</li>
	<li>More sorting options</li>
	<li>System for saving/viewing SP path images and descriptions (this may be obsolete with the ability to view paths from the wiki)</li>
	<li>Ability to view score rankings</li>
	<li>Automatic updater</li>
</ul>

